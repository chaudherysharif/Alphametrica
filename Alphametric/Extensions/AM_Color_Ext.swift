import Foundation

import UIKit


extension UIColor
{
    func getAppDarkColor() -> UIColor{
        return UIColor(red: 0.475341, green: 0.549713, blue: 0.608804, alpha: 1)
    }
    func getApplightColor() -> UIColor{
        return UIColor(red: 0.569959, green: 0.655426, blue: 0.717937, alpha: 1)
    }
    func getAppNavColor() -> UIColor{
        return UIColor(red: 0.359126, green: 0.41129, blue: 0.448881, alpha: 1)
    }
}
